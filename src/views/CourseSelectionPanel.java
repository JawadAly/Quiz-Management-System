        char firstChar = cBoxSelectCourse.charAt(0);
        int extractedCourseId = Character.getNumericValue(firstChar); 
        CourseSelectionPanel.selectedCourseId = extractedCourseId;
        //moving to StudentAttemptedQuiz Panel
        StudentAttemptedQuizzes pnl = new StudentAttemptedQuizzes();
        this.setVisible(false);
        pnl.setVisible(true);
