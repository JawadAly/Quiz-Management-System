    public void loadAllCoursesToCourseSelectionCBox(){
        //fetching all courses in this panels list
        courseInterface interfc = new courseImplements();
        CourseSelectionPanel.allCoursesList = interfc.getAllCoursesList();
        DefaultComboBoxModel cBoxModel = new DefaultComboBoxModel(); 
        for(Course singleCourse:CourseSelectionPanel.allCoursesList){
            cBoxModel.addElement(singleCourse.getCourseId()+" "+singleCourse.getCourseTitle());
        }
        attemptedQuizCourseSelectionCmbobox.setModel(cBoxModel);
    }

